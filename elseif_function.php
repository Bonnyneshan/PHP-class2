/*===========================
  The Use of (elseif) Start
===========================*/

$today = "sat";


if ($today == "sun") {
    echo "Sunday";

} elseif ($today == "mon") {
    echo "Monday";

} elseif ($today == "tue") {
    echo "Tuesday";

} elseif ($today == "wed") {
    echo "Wednesday";

} elseif ($today == "thu") {
    echo "Thursday";

} elseif ($today == "fri") {
    echo "Friday";

} elseif ($today == "sat") {
    echo "Saturday";

} else {
    echo "Nothing";
}

/*=========================
  The Use of (elseif) End
=========================*/

?>